		<h2>Qu'est-ce que la relecture de code?</h2>
		<article>
		<p>La relecture de code (code review, en anglais) est un outil fantastique qui
		permet d'améliorer grandement la qualité du code produit par une équipe de
		développement.</p>
		<p>Le concept est simple: écrire le code pour les collègues plutôt que pour la
		machine. En pratique, des outils comme gerrit code review ou review board facilitent
		la relecture et la discussion à propos du code.<p>
		</article>

		<h2>Les avantages de la relecture de code</h2>
		<article>
		<p>Lorsque le code est écrit pour être compris par les collègues plutôt que par
		l'ordinateur, les avantages sont immédiats:
		<ul>
			<li>Le code est compréhensible et donc plus facile à maintenir;</li>
			<li>Le code présente moins de bugs, puisque certains sont trouvés à la
			relecture;</li>
			<li>Le relecteur apprend de l'auteur à la lecture d'une
			solution particulièrement élégante;</li>
			<li>L'auteur apprend du relecteur lorsque celui-ci propose une meilleure
			manière de résoudre un problème;</li>
			<li>Le code suit le style décidé par l'équipe. Il est homogène et plus
			facile à lire;</li>
			<li>Les membres de l'équipe sont au courant des travaux de leurs collègues.</li>
		</ul>
		</article>

